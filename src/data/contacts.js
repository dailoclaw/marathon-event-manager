export const getContactById = (id) => contacts.find(c => c.id === id);

export const getContactsBySuburb = (suburb) => contacts.filter(c => c.suburb === suburb);

export const getContactsByCategory = (category) => contacts.filter(c => c.category === category);

export const getStats = () => {
  const total = contacts.length;
  const withEmail = contacts.filter(c => c.email).length;
  const withPhone = contacts.filter(c => c.phone).length;
  const responded = contacts.filter(c => c.response && c.response.toLowerCase() === 'y').length;
  const failed = contacts.filter(c => c.failed && c.failed.toLowerCase() === 'y').length;
  const sent = contacts.filter(c => c.date_sent && c.date_sent.includes('2025')).length;
  
  const byCategory = {};
  const bySuburb = {};
  
  contacts.forEach(c => {
    byCategory[c.category] = (byCategory[c.category] || 0) + 1;
    if (c.suburb) {
      bySuburb[c.suburb] = (bySuburb[c.suburb] || 0) + 1;
    }
  });
  
  return {
    total,
    withEmail,
    withPhone,
    responded,
    failed,
    sent,
    pending: total - sent,
    byCategory,
    bySuburb
  };
};

